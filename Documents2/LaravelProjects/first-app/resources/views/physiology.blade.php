            <h3>Lactate Threshold</h3>



            <p>            
                The lactate threshold is a supremely important physiological event for endurance athletes.  
                It is the intensity of exercise at which lactic acid levels measured in blood increase sharply on further increasing intensity.
                This is because if we progressively increase exercise intensity we are no longer able to get oxygen at a sufficient rate to supply all the energy needs 
                of an activity from the oxidation of glucose alone (aerobic metabolism). 
                Additional energy must come from the splitting glucose into lactate (glycolysis, anaerobic metabolism).
                Note that aerobic metabolism continues maximally but with an increasing additional anaerobic component with increasing work intensity.
                Although the requirement for energy is met, it comes at a heavy cost.  
                Lactic acid accumulation and associated blood pH reduction limits further activity.  
                It can only be cleared in a process that utilizes oxygen, and so cannot
                be eliminated until we reduce levels of activity again. (This is known as the oxygen debt). 
                The other problem is that glycolysis is highly inefficient and produces only a fraction (about <sup>1</sup>/<sub>15</sub>) of the energy produced from the metabolism of glucose with oxygen)
                It is wasteful of your precious glycogen reserves (you don't want to walk the last few miles of your marathon!)
            </p>
            <h4>VO<sub>2</sub> and VO<sub>2</sub> max</h4>
            <p>
                VO<sub>2</sub> is oxygen uptake in milliliters of oxygen per kilogram and so is a weight independent measure of how much oxygen an athlete is consuming at some point in time.
                VO<sub>2</sub> max is the maximum oxygen uptake possible in a particular athlete during a maximally intense activity.
            <p>            
                The graph below demonstrates the relationship between exercise intensity, VO<sub>2</sub>, VO<sub>2</sub> max and blood lactate.  
                The yellow arrow shows the increase in intensity of an activity and so energy consumption.  
                The red graph shows that initially oxygen consumption (VO<sub>2</sub>) keeps pace and we metabolize glucose aerobically and don't produce an excess of lactic acid.  
                As the intensity increases further we reach the limit at which our body is able to utilize oxygen (VO<sub>2</sub> max).  
                As we can no longer match our energy consumption through aerobic energy production we have to get part of our energy from glycolysis and lactate starts to rise.  
                This produces the typical "hockey stick" curve which is seen in the real world graph to the right.
            </p>

            <img class = "centerImage90" src = "Images\LactateThreshold.png" alt ="pic">



            <h4>VO<sub>2</sub> max and Lactate Threshold</h4>

            <p>
                There is clearly a relationship between VO<sub>2</sub> max and Lactate Threshold.  
                Above VO<sub>2</sub> max any sustained additional energy production must come from glycolysis 
                The reason the VO<sub>2</sub> at lactate threshold is not equal to VO<sub>2</sub> max is because the switch over to anaerobic metabolism happens progressively
                below VO<sub>2</sub> max. The rate of switchover is something that is very amenable to training as we will see below.
                We might choose to call the VO<sub>2</sub> at which lactate levels start to rise appreciably the VO<sub>2</sub> LT (Lactate Threshold).  
                It is somewhat lower then VO<sub>2</sub> max.
            </p>


            <p>           
                VO<sub>2</sub> max is heavily genetically determined both in its untrained level and its response to training.  
                The typical person can expect a modest maximal increase of up to 15%.  
                But there are much bigger gains to be made.  
                To understand this requires knowledge of the interrelationship between lactate threshold and VO<sub>2</sub> max as we get fitter.  
            </p>

            <img style="float:right; width: 40%; margin: 10px 0px 20px 50px;" src="Images/Lactate.png">

            <p>    

                Training makes lactate elimination more efficient.  
                This means that blood lactate is lower for a given intensity of exercise below VO<sub>2</sub> max.  
                Of course this reduction in lactate for a particular exercise intensity must ultimately fail as we approach and then exceed VO<sub>2</sub> max and so perhaps 
                frustratingly eventually bump up against our own genetic limitations.
            </p>
            <p>
                The biggest improvements in performance for most endurance athletes stem from being able to get close to and sustain activity just below VO<sub>2</sub> max,
                and to do so without rapidly metabolizing glucose (and therefore their glycogen reserves).
                This improvement is as a result of the change in shape of the lactate curve which increasingly develops the so called "hockey stick" with lactate levels only rising when close to VO<sub>2</sub> max.
                The recovery of lactate is an oxygen consuming process and so in part the modest increase in VO<sub>2</sub> max with training results from the 
                improvement in lactate metabolism.
                Representative lactate plots vs. %VO<sub>2(max)</sub> of an untrained and a trained athlete are shown above right for comparison.
            </p>

